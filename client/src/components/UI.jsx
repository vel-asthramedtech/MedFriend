import React from "react";

export function Toast({ toasts }) {
  if (!toasts?.length) return null;
  return (
    <div className="toast-wrap">
      {toasts.map((t, i) => (
        <div key={i} className={`toast toast-${t.type || "success"}`}>
          {t.type === "success" && "✓"}
          {t.type === "error" && "✕"}
          {t.type === "info" && "ℹ"}
          {t.msg}
        </div>
      ))}
    </div>
  );
}

export function Spinner({ lg }) {
  return <div className={`spinner${lg ? " spinner-lg" : ""}`} />;
}

export function LoadingCenter() {
  return (
    <div className="loading-center">
      <Spinner lg />
    </div>
  );
}

export function EmptyState({ icon, title, sub, action }) {
  return (
    <div className="empty-state">
      <div className="empty-icon">{icon || "📭"}</div>
      <div className="empty-title">{title}</div>
      {sub && <div className="empty-sub">{sub}</div>}
      {action && <div style={{ marginTop: 16 }}>{action}</div>}
    </div>
  );
}

export function StatusBadge({ status, testResults }) {
  if (status === "processing")
    return <span className="badge badge-processing">Processing</span>;
  if (status === "failed")
    return <span className="badge badge-failed">Failed</span>;
  if (status === "pending")
    return <span className="badge badge-pending">Pending</span>;
  if (status === "done") {
    const hasFlag = testResults?.some(
      (r) => r.status !== "normal" && r.status !== "unknown",
    );
    return (
      <span className={`badge ${hasFlag ? "badge-high" : "badge-done"}`}>
        {hasFlag ? "Flagged" : "Normal"}
      </span>
    );
  }
  return null;
}

